# exemplo de uso de FOR
# O loop abaixo exibe cada item de uma lista
print("--O loop abaixo exibe cada item de uma lista")

for item in ['Branco', 'Bia', 'André', 'Paula']:
    print(item)
''''laço FOR usando a função "in range". Você determina no parâmetro o limite da re
petição'''

print("--laço For usando a função in range. Você determina no parâmetro o limite da repetição: ")

for item in range(0, 10):
    print(item)

for item in range(5, 100, 50):
    print(item)



