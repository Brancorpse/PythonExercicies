# Função len serve para contar caracteres, contando os espaços

# pode-se determinar limites de caracteres de um campo

# essa função permite também contar itens de uma lista

curso = 'Curso em Python'
print(len(curso))

# Deixando caracteres em caixa baixa ou caixa alta

print('Apresentando em caixa alta')
print(curso.upper())
print('Apresentando em caixa baixa')
print(curso.lower())

# função find permite você achar qual posição está uma letra

print(curso.find('P'))


